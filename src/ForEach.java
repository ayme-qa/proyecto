public class ForEach {
    public static void main(String[] args){
        String[] nombres = new String[4];
        nombres[0] = "Gerardo";
        nombres[1] = "Jya";
        nombres[2] = "Eyleen";
        nombres[3] = "Andrea";
        for (String nombre: nombres){
            System.out.println(nombre + ", ");}

    }
}
